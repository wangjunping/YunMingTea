package com.imcore.yunmingtea.ui;

public class Commodity {
   private String ImageUrl;
   private String ProductName;
   private long Price;
   private long SaleTotal;
   private long FavotieTotal;
public String getImageUrl() {
	return ImageUrl;
}
public void setImageUrl(String imageUrl) {
	ImageUrl = imageUrl;
}
public String getProductName() {
	return ProductName;
}
public void setProductName(String productName) {
	ProductName = productName;
}
public long getPrice() {
	return Price;
}
public void setPrice(long price) {
	Price = price;
}
public long getSaleTotal() {
	return SaleTotal;
}
public void setSaleTotal(long saleTotal) {
	SaleTotal = saleTotal;
}
public long getFavotieTotal() {
	return FavotieTotal;
}
public void setFavotieTotal(long favotieTotal) {
	FavotieTotal = favotieTotal;
}

    
}
